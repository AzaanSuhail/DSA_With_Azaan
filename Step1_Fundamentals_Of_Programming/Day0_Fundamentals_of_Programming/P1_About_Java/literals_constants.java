public class literals_constants {
    public static void main(String[] args) {
        //Type1  : Integer data type
        int x = 101;
        System.err.println(x);

        //Type2 : Character data type
        char y = 'a';
        System.err.println(y);

        //Type3 : Boolean data type
        boolean z = true;
        System.err.println(z);

        //Type4 : String data type
        String s = "Hello World";
        System.err.println(s);

        //Type5 : Float data type
        float f = 3.14f;
        System.err.println(f);

        //Type6 : Double data type
        double d = 3.14;
        System.err.println(d);


        //Type7 : Long data type
        long l = 123456789L;   // st
        System.err.println(l);

    

    }
}
