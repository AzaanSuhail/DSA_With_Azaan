public class P5_loops {
    public static void main(String[] args) {
        // Loops
        // 1. for
        // 2. while


        int i = 0; // ^ initialization

        while (i < 1000) {
           // The code snippet `System.out.println("Shrishti");` is printing the text "Shrishti" to the
           // console.
            System.out.println("Shrishti");

            i++; // * 0 -> 1 --> 2 --> 3
        }

        // ? efficient/simpler version

        for (int j = 0; j < 1000; j++) {
            System.out.println("Amit❤️Shrishti"); //j=0 --> 1
        }

    }
}
